								<h2 class="major">About</h2>
								<span class="image main"><img src="themes/denison/images/pic01.jpg" alt="" /></span>
								<p>Welcome to Progetto Vitaliano (aka the VITAL Project: Videos for Italian Teaching and Learning.) This open educational resource (OER) database has been developed for both students and teachers of Italian to easily access culturally authentic videos to use in and outside of the classroom. We live in an interconnected, globalized world that produces information constantly, so much of which can be harnessed to make foreign language teaching and learning powerfully relevant in students’ lives, connecting them with the real world through authentic displays of language and culture.</p><p> Teachers and students using this database can find relevant video content to use for lessons, activities, and class projects that is tagged for content, grammatical topic, audience level, language level, audio language, and subtitle information.</p><p>This database is open and free to the public, and it is crowd-sourced— which means <i>we need your help</i>. Please <a href="https://docs.google.com/forms/d/e/1FAIpQLSe2vsMYkV3Z9Wg5Y2vZj2am-kFnO_qlUJFSPuBpyBYluJChfg/viewform?c=0&w=1">consider contributing a resource</a> to grow this collection of videos for the benefit of our students’ linguistic and cultural journeys.</p><p>In 2018, the next phase of this database will be launched. This second phase will include a principled lesson plan resource addition in which teachers will be able to contribute a lesson plan or project to accompany a video or set of videos.</p><p>Please <a href="mailto:progettovitaliano@gmail.com">email progettovitaliano@gmail.com</a> with any questions, comments, and for more information.</p><h3>Credit and Acknowledgements:</h3><p>This project was funded in January 2017 by the University of Oklahoma College of Arts and Science Instructional Technology Grant. Many thanks go to Dr. Bruce Mason and the OU Instructional Technology Grant Committee, as well as Dean Kelly Damphousse for the generous grant to fund this pilot project. This project would also not be possible without the invaluable help of Dr. John Stewart, Tara Carlisle, Sarah Clayton, Chiara Fontani, and Alejandra Acuna Balbuena. A very special thank you to Dr. Barbara Bird for beginning this idea many years ago and for always looking toward the future of Italian pedagogy.</p><p>Progetto Vitaliano was founded by <a href="http://modlang.ou.edu/daniela-busciglio">Dr. Daniela Busciglio</a>, Assistant Professor of Italian and Second Language Acquisition and Italian Language Program Director at the University of Oklahoma.</p><p>All front page images used under <a href="https://creativecommons.org/licenses/by/2.0/">Creative Commons By 2.0 license:</a></a></p><ul><li>Canal Grande image courtesy of <a href="https://www.flickr.com/photos/worldaroundtrip/15702819385/">worldaroundtrip</a></li><li>Manarola image courtesy of <a href="https://www.flickr.com/photos/hihaa/">Fougerouse Arnaud</a></li></ul></p>
